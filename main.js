var makeMap = function(map) {
  for (var i=0; i < map.length; i++) {
    var row = 16 * i;
    for (var j=0; j < map[0].length; j++) {
      var col = 16 * j;
      ctx.beginPath();
      ctx.rect(row, col, tileSize, tileSize);
      ctx.fillStyle = tiles[map[i][j]];
      ctx.fill();
      ctx.closePath();
    }
  }
}

